USE LARABOX
GO
CREATE TABLE USUARIOS(
    ID_USUARIO INT PRIMARY KEY IDENTITY(1,1),
    USUARIO VARCHAR(25) NOT NULL UNIQUE,
    CLAVE VARCHAR(100) NOT NULL
)
GO
CREATE TABLE PAIS(
    ID_PAIS VARCHAR(2) PRIMARY KEY,
    NOMBRE_PAIS VARCHAR(50) NOT NULL,
    NUMERICO INT NOT NULL CHECK(NUMERICO > 0)
)
GO
CREATE TABLE LOCALIDAD(
    COD_LOCALIDAD INT PRIMARY KEY IDENTITY(1,1),
    LOCALIDAD VARCHAR(85) NOT NULL,
    PROVINCIA VARCHAR(50) NOT NULL,
    PAIS VARCHAR(2) FOREIGN KEY REFERENCES PAIS(ID_PAIS)
)
GO
CREATE TABLE DATOS_PERSONALES(
    ID_PERSONA INT PRIMARY KEY IDENTITY(1,1),
    APELLIDO VARCHAR(40) NOT NULL,
    NOMBRES VARCHAR(60) NOT NULL,
    MAIL VARCHAR(100) NOT NULL,
    TELEFONO VARCHAR(20) NULL,
    CELULAR VARCHAR(20) NULL,--+540348767997295
    FECHA_NAC DATE NULL,
    DOMICILIO VARCHAR(250) NULL,
    LOCALIDAD INT NULL FOREIGN KEY REFERENCES LOCALIDAD(COD_LOCALIDAD),
    USUARIO VARCHAR(25) NULL FOREIGN KEY REFERENCES USUARIOS(USUARIO)
)
GO
CREATE TABLE ARCHIVOS(
    ID_ARCHIVOS INT PRIMARY KEY IDENTITY(1,1),
    USUARIO VARCHAR(25) NULL FOREIGN KEY REFERENCES USUARIOS(USUARIO),
    TAMAÑO_KB INT NOT NULL CHECK(TAMAÑO_KB > 0),
    NOMBRE VARCHAR(100) NOT NULL,
    EXTENSION VARCHAR(8) NOT NULL,
    FECHA_CREACION DATE NOT NULL CHECK(FECHA_CREACION <= GETDATE()),
    FECHA_MODIFICACION DATE NOT NULL CHECK(FECHA_MODIFICACION <= GETDATE()),
    ESTADO BIT NOT NULL DEFAULT(1),
    CONSTRAINT CHECK_DATE CHECK(FECHA_CREACION <= FECHA_MODIFICACION)
)
GO


